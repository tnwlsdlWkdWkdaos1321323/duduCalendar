DELIMITER //
CREATE FUNCTION `next_sequence_value`(`seq_name` VARCHAR(50)
) RETURNS varchar(50) CHARSET utf8mb4 COLLATE utf8mb4_general_ci
BEGIN
    DECLARE seq_val INT;
    DECLARE formatted_seq_val VARCHAR(50);
    
    UPDATE schdul_sequence
    SET sequence_value = LAST_INSERT_ID(sequence_value + 1)
    WHERE sequence_name = seq_name;
    
    SELECT LAST_INSERT_ID() INTO seq_val;
    
    SET formatted_seq_val = CONCAT('SCHDUl_', LPAD(seq_val, 6, '0'));
    
    RETURN formatted_seq_val;
END//
DELIMITER ;

